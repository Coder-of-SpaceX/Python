# String
a = "RadhaKrishna"
print(a)
print(type(a))

# Integer
b = 10
print(b)
print(type(b))

# Float
c = 14.5
print(c)
print(type(c))

# Complex
d = 1 + 6j
print(d)
print(type(d))

# List
e = ["Brahma", "Vishnu", "Mahesh"]
print(e)
print(type(e))

# Tuple
f = ("Brahma","Vishnu","Mahesh")
print(f)
print(type(f))

# Range
g = range (0,10,2)
print(g)
print(type(g))

# Dict
h = {"name" : "Radha", "Age": 26}
print(h)
print(type(h))

# Set
i = {"Parwati","Shiv"}
print(i)
print(type(i))

# Frozenset
j = frozenset({"Radha","Krishna"})
print(j)
print(type(j))

# Boolean
k = True
print(k)
print(type(k))

# Bytes
l = b"Hello"
print(l)
print(type(l))

# Bytearray
m = bytearray(10)
print(m)
print(type(m))

# Memory View Bytes
n = memoryview(bytes(10))
print(n)
print(type(n))

# none
o = None
print(o)
print(type(o))